class Animal:    
    def __init__(self, name, type, dob, age, notes, owner, owner_tel, owner_email, vet, id = None):
        self.name = name
        self.type = type
        self.dob = dob
        self.age = age
        self.notes = notes
        self.owner = owner
        self.owner_tel = owner_tel
        self.owner_email = owner_email
        self.vet = vet
        self.id = id